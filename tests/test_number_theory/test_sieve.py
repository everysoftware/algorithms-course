import pytest

from number_theory import sieve


@pytest.mark.parametrize(
    "n, expected",
    [
        (1, []),
        (2, [2]),
        (3, [2, 3]),
        (4, [2, 3]),
        (5, [2, 3, 5]),
        (6, [2, 3, 5]),
        (7, [2, 3, 5, 7]),
        (8, [2, 3, 5, 7]),
        (9, [2, 3, 5, 7]),
        (10, [2, 3, 5, 7]),
        (11, [2, 3, 5, 7, 11]),
        (12, [2, 3, 5, 7, 11]),
        (13, [2, 3, 5, 7, 11, 13]),
        (14, [2, 3, 5, 7, 11, 13]),
        (15, [2, 3, 5, 7, 11, 13]),
        (16, [2, 3, 5, 7, 11, 13]),
        (17, [2, 3, 5, 7, 11, 13, 17]),
        (18, [2, 3, 5, 7, 11, 13, 17]),
        (19, [2, 3, 5, 7, 11, 13, 17, 19]),
        (20, [2, 3, 5, 7, 11, 13, 17, 19]),
        (21, [2, 3, 5, 7, 11, 13, 17, 19]),
        (22, [2, 3, 5, 7, 11, 13, 17, 19]),
        (23, [2, 3, 5, 7, 11, 13, 17, 19, 23]),
        (24, [2, 3, 5, 7, 11, 13, 17, 19, 23]),
        (25, [2, 3, 5, 7, 11, 13, 17, 19, 23]),
    ],
)
def test_sieve(n: int, expected: list[int]) -> None:
    assert sieve(n) == expected
